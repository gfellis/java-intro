    private void txtInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyReleased
        // When a new key is entered into the text field, this block executes        
        
        txtInput.setBackground(Color.white);
        // Try/Catch to check for valid integer
        try {                        
            if (!(txtInput.getText().isEmpty() || txtInput.getText().startsWith("-") && txtInput.getText().length() == 1)) {
                int intInput = Integer.parseInt(txtInput.getText());
                
                // Set the text to the return value of reverseDigit();
                txtOutput.setText(String.valueOf(reverseDigit(intInput)));                
            }
        } catch (NumberFormatException nfe) {
            // If the number is not a valid integer, set background to red
            // and set the output text as a warning
            txtInput.setBackground(Color.red);
            txtOutput.setText("Invalid number");
        }
    }//GEN-LAST:event_txtInputKeyReleased

