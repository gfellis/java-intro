    public int reverseDigit(int num) {
        // Take the user provided value from num
        // Output the reverse number
        
        // split the number into a string
        String[] input = Integer.toString(num).split("");
        String output = ""; // setup new output                
        
        // Loop through the length of the number provided in reverse
        for (int i = input.length - 1; i >= 0; i--) {
            // If the number is a negative, add this to the front
            // to avoid NumberExceptionErrors on return
            if (input[i].equals("-")) {
                output = "-" + output;
            } else {
                // Add the values together in reverse order
                output += input[i];
            }
        }
        
        // Send the parsed Integer back from the output string
        return Integer.parseInt(output);
    }    

